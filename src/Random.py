    # to make the sequence many times, following is repeated.
    def getRandom(self):
        MOVE = 250
        if (self.cnt > self.N_RAND):
            for i in range(1, int(self.N_RAND / 100) + 1):
                k = (i - 1) * 100
                for j in range(1 + k + MOVE, k + 100 + MOVE + 1):
                    self.randArray[j] = self.randArray[j - 250] ^ self.randArray[j - 103]
                    # print("randArray["+str(j)+"]:",self.randArray[j])

            for j in range(-249 + MOVE, 0 + MOVE + 1):
                self.randArray[j] = self.randArray[j + self.N_RAND]
            self.cnt = 1

        randomValue = self.randArray[self.cnt + MOVE] * self.A_NORM
        self.cnt += 1
        return randomValue

